                        <h2 class="teal-heading">Phone Scams</h2>
                        <p>Phone scams involve fraudsters calling individuals pretending to be from legitimate organizations to extract personal information or money. These scams can take various forms, including:</p>
                        <ul>
                            <li><strong>Imposter Scams:</strong> The scammer pretends to be someone you trust, like a government official, tech support, or even a family member, and tries to convince you to give them money or personal information.</li>
                            <li><strong>Tech Support Scams:</strong> The scammer claims to be from a well-known tech company and says there's a problem with your computer. They then ask for remote access or payment to fix the non-existent issue.</li>
                            <li><strong>Lottery and Prize Scams:</strong> The scammer tells you that you've won a prize or lottery but need to pay fees or taxes upfront to claim it.</li>
                            <li><strong>Charity Scams:</strong> The scammer solicits donations for fake charities, often exploiting recent disasters or tragedies to seem legitimate.</li>
                            <li><strong>Debt Collection Scams:</strong> The scammer pretends to be a debt collector and demands payment for a fake debt, often using threats of arrest or legal action.</li>
                            <li><strong>Utility Scams:</strong> The scammer claims to be from a utility company and threatens to shut off your service unless you make an immediate payment.</li>
                        </ul>
                        <p>For more details on identifying and protecting yourself from phone scams, watch this informative video:</p>
