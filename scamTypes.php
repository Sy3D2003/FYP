                        <h2 class="teal-heading">Phishing Scams</h2>
                        <p>Phishing scams are attempts by fraudsters to steal personal information such as passwords, credit card numbers, and other sensitive data by pretending to be a trustworthy entity. These scams can take various forms, including:</p>
                        <ul>
                            <li><strong>Email Phishing:</strong> Attackers send emails that appear to come from legitimate sources like banks, social media sites, or online services. These emails often contain links to fake websites designed to steal personal information.</li>
                            <li><strong>Spear Phishing:</strong> A targeted form of phishing where attackers focus on a specific individual or organization. These emails are often personalized and appear to come from someone the victim knows.</li>
                            <li><strong>Clone Phishing:</strong> A legitimate email that was previously delivered is cloned and modified with malicious links or attachments. The attacker then sends this cloned email to the victim.</li>
                            <li><strong>Whaling:</strong> A form of phishing that targets high-profile individuals within an organization, such as executives or other senior officials.</li>
                            <li><strong>Smishing:</strong> Phishing attempts carried out through SMS (text messages). These messages often contain malicious links or ask for personal information under the guise of a trusted entity.</li>
                            <li><strong>Vishing:</strong> Phishing conducted via voice calls. Attackers might pose as representatives from banks, government agencies, or tech support services to extract personal information from victims.</li>
                        </ul>
