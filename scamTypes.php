                            <li><strong>Romance Scams:</strong> Fake profiles on dating sites or social media to build romantic relationships and then ask for money.</li>
                            <li><strong>Employment Scams:</strong> Fake job opportunities to steal money or personal information.</li>
                            <li><strong>Charity Scams:</strong> Fake charity solicitations to steal donations.</li>
